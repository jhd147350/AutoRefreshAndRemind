package jhd.page;

public class PageResult {

	private boolean hasNewTicket;
	private String title;
	private String name;

	public boolean isHasNewTicket() {
		return hasNewTicket;
	}

	public void setHasNewTicket(boolean hasNewTicket) {
		this.hasNewTicket = hasNewTicket;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
